package com.xworkz.typecastingtypeconverting;

public class Cast2 {
	
	
	public static void main(String[] args) {
		
		
		
		//typeCasting
			short a;
			int b= 30;
			a=(short)b;
			
			
		//typeConverting
			short c= 40;
			int d;
			d=c;
			
			System.out.println(a);
			System.out.println(d);
		}


}
