package com.xworkz.vegetable.dto;

public class Vitamins {
	
	String vita1;
	String vita2;
	String vita3;
	String vita4;
	public Vitamins(String vita1, String vita2, String vita3, String vita4) {
		super();
		this.vita1 = vita1;
		this.vita2 = vita2;
		this.vita3 = vita3;
		this.vita4 = vita4;
	}
	public String toString() {
		return "Vitamins [vita1=" + vita1 + ", vita2=" + vita2 + ", vita3=" + vita3 + ", vita4=" + vita4 + "]";
	}
	
	

}
